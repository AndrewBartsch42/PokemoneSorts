class pokemon:
    def __init__(self, number, name, type1, type2, total, hp, attack, defense, spatk, spdef, speed, gen, legendary):
        self.number = number
        self.name = name
        self.type1 = type1
        self.type2 = type2
        self.total = total
        self.hp = hp
        self.attack = attack
        self.defense = defense
        self.spatk = spatk
        self.spdef = spdef
        self.speed = speed
        self.gen = gen
        self.legendary = legendary